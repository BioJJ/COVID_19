    public void Buscar_cep() {

        if (cep.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "ATENÇÃO, preencha todos os campos");
        } else {
            WebServiceCep web = WebServiceCep.searchCep(cep.getText());

            if (web.wasSuccessful()) {
                rua.setText(web.getLogradouroFull());
                bairro.setText(web.getBairro());
                cidade.setText(web.getCidade());
                estado.setText(web.getUf());
            } else {
                JOptionPane.showMessageDialog(null, "CEP invalido");

                cep.setText("");
                cep.requestFocus();
            }
        }

    }

